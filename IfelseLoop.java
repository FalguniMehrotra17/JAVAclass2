package JAVA_PRACTICE;

public class IfelseLoop {
	public static void main(String args[]) {
		int a=10,b=20,c=30;
		if (a>b && a>c) 
			System.out.print("a is the greatest number" + "\t"+a);
		
		else if 
			(b>a && b>c) 
				System.out.print("b is the greatest number" + "\t"+b);
			
		
		else 
			System.out.print("c is the greatest number" + "\t"+c);
		
	}

}
